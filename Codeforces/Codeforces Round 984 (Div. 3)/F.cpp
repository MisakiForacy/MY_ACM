#include <bits/stdc++.h>
using namespace std;
using LL = long long;
LL XOR(LL x){
    if (x % 4 == 0) return x;
    if (x % 4 == 1) return 1;
    if (x % 4 == 2) return x + 1;
    if (x % 4 == 3) return 0;
}
int main(){
    int T;
    cin >> T;
    while (T--){
        LL l,r,i,k;
        cin >> l >> r >> i >> k;
        LL L,R;
        if (l-1 >= k){
            L = XOR(l-1) ^ (((XOR(l-1-k >> i)) << i)) ^ (k * !((l-k-1 >> i)%2));
        } else{
            L = XOR(l-1);
        }
        if (r >= k){
            R = XOR(r) ^ (((XOR(r-k >> i)) << i)) ^ (k * !((r-k >> i)%2));
        } else{
            R = XOR(r);
        }
        // cout << L << ' ' << R << '\n';
        cout << (L ^ R) << '\n';
    }
}

